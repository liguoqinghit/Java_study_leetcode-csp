package 周赛165;

public class Solution_1278 {
    public static int palindromePartition(String s, int k) {
        int num = 0;
        if (s.length() == k) return num;

        return num;
    }

    public static void main(String[] args) {
        String s = "abc";
        int k = 3;
        System.out.println(palindromePartition(s, k));
    }
}
